<script>
  // Add an event listener to the accordion buttons
  document.addEventListener('click', function (event) {
    if (!event.target.matches('.accordion-button')) return;
    
    // Get the data-bs-parent value to find the parent accordion
    var parent = document.querySelector(event.target.getAttribute('data-bs-parent'));
    
    // Get all collapse elements within the parent
    var collapses = parent.querySelectorAll('.collapse');
    
    // Hide all other collapses except the target collapse
    collapses.forEach(function (collapse) {
      if (collapse !== event.target.getAttribute('data-bs-target')) {
        collapse.classList.remove('show');
      }
    });
  });
</script>

